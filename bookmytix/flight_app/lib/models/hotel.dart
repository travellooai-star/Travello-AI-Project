class Hotel {
  final String id;
  final String name;
  final String address;
  final String city;
  final double rating;
  final int totalReviews;
  final List<String> images;
  final List<String> amenities;
  final double pricePerNight; // Starting from price
  final String category; // '3-Star', '4-Star', '5-Star', 'Budget'
