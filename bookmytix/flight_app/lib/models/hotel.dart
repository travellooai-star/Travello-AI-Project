  static List<Hotel> searchHotels({
    required String city,
    String? category,
    double? maxPrice,
    double? minRating,
    bool? hasPool,
    bool? hasBreakfast,
  }) {
    var hotels = getHotels(city);

    if (category != null && category.isNotEmpty) {
      hotels = hotels.where((h) => h.category == category).toList();
    }

    if (maxPrice != null) {
      hotels = hotels.where((h) => h.pricePerNight <= maxPrice).toList();
    }

    if (minRating != null) {
      hotels = hotels.where((h) => h.rating >= minRating).toList();
    }

    if (hasPool == true) {
      hotels = hotels.where((h) => h.hasPool).toList();
    }

    if (hasBreakfast == true) {
      hotels = hotels.where((h) => h.hasBreakfast).toList();
    }

    // Sort by rating (highest first)
    hotels.sort((a, b) => b.rating.compareTo(a.rating));

    return hotels;
  }
